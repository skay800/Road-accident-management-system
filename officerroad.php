                <h2>Bleeding nose</h2>
                <p>Sit upright and lean forward. By remaining upright, you reduce blood pressure in the veins of your nose. This discourages further bleeding. ...
Gently blow your nose. Blow your nose to clear your nose of blood clots. Then spray both sides of your nose with a nasal decongestant containing oxymetazoline (Afrin).
Pinch your nose. Use your thumb and index finger to pinch your nostrils shut. Breathe through your mouth. Continue to pinch for 10 to 15 minutes. 
To prevent re-bleeding, don't pick or blow your nose and don't bend down for several hours. Keep your head higher than the level of your heart. 
If re-bleeding occurs, go through these steps again. Call your doctor if the bleeding continues.</p>
