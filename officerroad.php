                <h2>Deep cut</h2>
                <p>Bleeding should be controlled through the use of a compress. This may be contraindicated though to eye injuries, head injuries, and to lacerations with embedded object on it.  The affected area can also be elevated above the heart level to stop bleeding.
Apply bandages.  Generally, bandages help to keep the wound from infections, and provide support to the area.  Remember not to apply the bandage too tightly as it can impede the circulation.
The first aid responder should care for shock.  In cases when the patient has become unconscious, CPR may be performed.
Finally, medical treatment should be sought as possible.  The affected are may be stitched or tetanus toxoid injection may be given to prevent infection.</p>
